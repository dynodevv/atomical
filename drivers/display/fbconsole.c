/* Draw a single character glyph at pixel position (px, py) */
static void draw_char(uint32_t px, uint32_t py, char c, uint32_t fg, uint32_t bkg)
{
    if (c < 32 || c > 126)
        c = '?';

    const uint8_t *glyph = font_data[c - 32];
    uint8_t *fb_base = (uint8_t *)console_fb->address;

    for (int row = 0; row < FONT_HEIGHT; row++) {
        uint32_t y = py + (uint32_t)row;
        if (y >= console_fb->height)
            break;

        uint32_t *pixel = (uint32_t *)(fb_base + y * console_fb->pitch + px * 4);
        uint8_t bits = glyph[row];

        for (int col = 0; col < FONT_WIDTH; col++) {
            if (px + (uint32_t)col >= console_fb->width)
                break;
            pixel[col] = (bits & (0x80 >> col)) ? fg : bkg;
        }
    }
}

/* Scroll the screen up by one text row */
static void scroll_up(void)
{
    uint8_t *fb_base = (uint8_t *)console_fb->address;
    uint32_t row_bytes = console_fb->pitch * FONT_HEIGHT;
    uint32_t total_bytes = console_fb->pitch * console_fb->height;

    /* Move everything up by one text row */
    memmove(fb_base, fb_base + row_bytes, total_bytes - row_bytes);

    /* Clear the last row */
    uint32_t last_row_start = (max_rows - 1) * FONT_HEIGHT;
    for (uint32_t y = last_row_start; y < console_fb->height; y++) {
        uint32_t *pixel = (uint32_t *)(fb_base + y * console_fb->pitch);
        for (uint32_t x = 0; x < console_fb->width; x++) {
            pixel[x] = bg_color;
        }
    }
}

void fbcon_init(hal_framebuffer_t *fb)
{
    if (!fb || !fb->address || fb->bpp != 32)
        return;

    console_fb = fb;
    max_cols = fb->width / FONT_WIDTH;
    max_rows = fb->height / FONT_HEIGHT;
    cursor_x = 0;
    cursor_y = 0;
    initialized = true;

    fbcon_clear();
}

void fbcon_putchar(char c)
{
    if (!initialized)
        return;

    switch (c) {
    case '\n':
        cursor_x = 0;
        cursor_y++;
        break;
    case '\r':
        cursor_x = 0;
        break;
    case '\t':
        cursor_x = (cursor_x + 4) & ~3u;
        break;
    case '\b':
        if (cursor_x > 0) {
            cursor_x--;
            draw_char(cursor_x * FONT_WIDTH, cursor_y * FONT_HEIGHT, ' ', fg_color, bg_color);
        }
        break;
    default:
        if (c >= 32 && c <= 126) {
            draw_char(cursor_x * FONT_WIDTH, cursor_y * FONT_HEIGHT, c, fg_color, bg_color);
            cursor_x++;
        }
        break;
    }

    /* Wrap at end of line */
    if (cursor_x >= max_cols) {
        cursor_x = 0;
        cursor_y++;
    }

    /* Scroll if needed */
    if (cursor_y >= max_rows) {
        scroll_up();
        cursor_y = max_rows - 1;
    }
}

void fbcon_puts(const char *s)
{
    if (!s)
        return;
    while (*s)
        fbcon_putchar(*s++);
}

void fbcon_clear(void)
{
    if (!initialized)
        return;

    uint8_t *fb_base = (uint8_t *)console_fb->address;
    for (uint32_t y = 0; y < console_fb->height; y++) {
        uint32_t *pixel = (uint32_t *)(fb_base + y * console_fb->pitch);
        for (uint32_t x = 0; x < console_fb->width; x++) {
            pixel[x] = bg_color;
        }
    }

    cursor_x = 0;
    cursor_y = 0;
}

void fbcon_set_color(uint32_t fg, uint32_t bg)
{
    fg_color = fg;
    bg_color = bg;
}

bool fbcon_is_active(void)
{
    return initialized;
}
